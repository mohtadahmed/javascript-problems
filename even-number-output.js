// Problem Statement

// Write a program to show all the even number between 412 to 456.

/*
-----------------------
=======================
        Solution
=======================
-----------------------
*/

// Using While Loop

var number = 412;

while (number <= 456) {
    console.log(number);
    number += 2;
}


//  Using For Loop

for (let i = 412; i <= 456; i += 2) {
    console.log(i);
}