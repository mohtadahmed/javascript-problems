// Problem Statement

// Write a program that will show all the numbers between 58 to 98.


/*
-----------------------
=======================
        Solution
=======================
-----------------------
*/

// Using While Loop

var number = 58;

while (number <= 98) {
    console.log(number);
    number++;
}


// Using For Loop

for (var i = 58; i <= 98; i++){
    console.log(i);    
}